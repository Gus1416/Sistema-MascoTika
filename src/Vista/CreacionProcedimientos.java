    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int randomnum = ThreadLocalRandom.current().nextInt(01, 98 + 1);
        String IDProcedimiento = "P" + randomnum;
        String Tipo = jTextField2.getText();
        int MontoUnitario = Integer.parseInt(jTextField3.getText());
        int MontoServAdom = Integer.parseInt(jTextField4.getText());
        String DescripcionProcedimiento = jTextField5.getText();
        int CodigoCategoria = Integer.parseInt(jTextField8.getText());
        int IDPaciente = Integer.parseInt(jTextField7.getText());
        String Nombre = jTextField9.getText();

        Procedimiento proced = new ProcedimientoDAO().insertar(IDProcedimiento, Tipo, MontoUnitario, MontoServAdom, DescripcionProcedimiento, CodigoCategoria, IDPaciente, Nombre);
        if (proced == null) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo crear el registro en la base de datos");
            return;
        }

        JOptionPane.showMessageDialog(rootPane, "Registro Creado");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField8.setText("");
        jTextField7.setText("");
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed


