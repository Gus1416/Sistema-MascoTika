    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
     
        String IDPaciente = jTextField1.getText();
        String Raza = jTextField2.getText();
        String Nombre = jTextField3.getText();
        String Edad= jTextField4.getText();
        
        if(IDPaciente.equals("") || Raza.equals("") || Nombre.equals("") || Edad.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
            return;
        }
        int paci = new PacienteDAO().actualizar(Integer.parseInt(IDPaciente), Raza, Nombre, Integer.parseInt(Edad));
        if (paci == 0){
           JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar la informacion");
           return; 
        }
        JOptionPane.showMessageDialog(rootPane, "Informacion del paciente "+IDPaciente+" actualizada");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed


