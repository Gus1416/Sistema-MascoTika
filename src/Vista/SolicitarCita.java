    public void ObtenerDatos(){
        List<Procedimiento> procedimientos = new ProcedimientoDAO().obtenerRegistrosLista();
        DefaultTableModel modelo = new DefaultTableModel();
        String [] columnas= {"Nombre", "ID", "Tipo", "Monto","Monto Domicilio","Descripcion"};
        modelo.setColumnIdentifiers(columnas);
        for(Procedimiento proced : procedimientos){
            String [] renglon = {proced.getNombreProcedimiento(),
                proced.getIDProcedimiento(),
                proced.getTipo(),
                Integer.toString(proced.getMontoUnitario()),
                Integer.toString(proced.getMontoServAdom()),
                proced.getDescripcionProcedimiento(),
                Integer.toString(proced.getCodigoCategoria()),
                Integer.toString(proced.getIDPaciente())    };
            modelo.addRow(renglon);
            }
       jTable1.setModel(modelo);
        }
    
    public void ObtenerDatosVet(String IDProcedimiento){
        List<Veterinario> veterinarios = new VeterinarioDAO().ObtenerVetProcedimiento(IDProcedimiento);
        DefaultTableModel modelo = new DefaultTableModel();
        String [] columnas= {"Cedula", "ID", "Nombre", "Apellido", "Calificacion","Horario","Sitio Web"};
        modelo.setColumnIdentifiers(columnas);
        for(Veterinario veterinario : veterinarios){
            String [] renglon = {
                Integer.toString(veterinario.getCedula()),
                veterinario.getIDVet(),
                veterinario.getNombre(),
                veterinario.getApellido(),
                Integer.toString(veterinario.getCalificacion()),
                veterinario.getHorarioAtencion(),
                veterinario.getSitioWeb()
            };
            modelo.addRow(renglon);
            
            }
       jTable2.setModel(modelo);
        }
    
    public String Latitud (int Cedula){
        List<UbicacionPersona> latitudes = new UbicacionPersonaDAO().consultarLatitud(Cedula);
        String lat = "";
        for (UbicacionPersona latitud: latitudes){
            lat = latitud.getLatitud();
            //System.out.println(lat + "   Latitud");
        }
       return lat;        
    }
    
    
    public String Longitud (int Cedula){
        //System.out.println(Cedula);
        List<UbicacionPersona> longitudes = new UbicacionPersonaDAO().consultarLongitud(Cedula);
        String lon = "";
        for (UbicacionPersona longitud : longitudes){
            lon = longitud.getLongitud();
            //System.out.println(lon);
        }
        return lon;        
    }
    

