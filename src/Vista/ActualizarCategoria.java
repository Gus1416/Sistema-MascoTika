    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String CodigoCategoria = Integer.toString(this.CodigoCategoria);
        String NombreCategoria = jTextField3.getText();
        String Estado = jTextField4.getText();
        String DescripcionCategoria = jTextField4.getText();
        String TipoCategoria = jTextField5.getText();
        
        if(CodigoCategoria.equals("") || NombreCategoria.equals("") || Estado.equals("") || DescripcionCategoria.equals("") || TipoCategoria.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
            return;
        }
        int catego = new CategoriaServicioDAO().actualizar(Integer.parseInt(CodigoCategoria), NombreCategoria, Estado, DescripcionCategoria, TipoCategoria);
        if (catego == 0){
           JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar la informacion");
           return; 
        }
        JOptionPane.showMessageDialog(rootPane, "Informacion de la Categor√≠a "+CodigoCategoria+" actualizada");
        //jTextField1.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed


