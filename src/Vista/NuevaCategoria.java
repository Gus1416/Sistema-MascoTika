    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        int randomnum = ThreadLocalRandom.current().nextInt(01, 98 + 1);
        String CodigoCategoria = Integer.toString(randomnum);
        String NombreCategoria = jTextField2.getText();
        String Estado = jTextField3.getText();
        String DescripcionCategoria = jTextField4.getText();
        String TipoCategoria = jTextField5.getText();

        if (CodigoCategoria.equals("") || NombreCategoria.equals("") || Estado.equals("") || DescripcionCategoria.equals("") || TipoCategoria.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
            return;
        }
        CategoriaServicio catego = new CategoriaServicioDAO().insertar(Integer.parseInt(CodigoCategoria), NombreCategoria, Estado, DescripcionCategoria, TipoCategoria);
        if (catego == null) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo registrar la nueva categoria");
            return;
        }
        JOptionPane.showMessageDialog(rootPane, "Nueva Categoria agregada");

        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        this.setVisible(false);

        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


